        k_t = np.log1p(R_p_t) / R_p_t
        k_t = k_t.fillna(1.0); k_t[R_p_t == 0] = 1.0
        adj_t = k_t / K

        inc_c = (inc_c_t * adj_t).sum() * scale_factor
        app_c = (app_c_t * adj_t).sum() * scale_factor
        bm_c = (bm_c_t * adj_t).sum() * scale_factor
        return inc_c, app_c, inc_c + app_c, bm_c

