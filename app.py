import glob
import os
import pandas as pd
import numpy as np

FOLDER_PATH = '/content/drive/MyDrive/Colab projects/'
file_pattern = os.path.join(FOLDER_PATH, 'Performance-Master*.xlsx')
files_to_process = glob.glob(file_pattern)

files_to_process = [f for f in files_to_process if not os.path.basename(f).startswith('Processed_')]

if not files_to_process:
    print(f"No files found matching '{file_pattern}'. Please check your folder path.")
else:
    for file_path in files_to_process:
        print(f"Processing: {os.path.basename(file_path)}...")
        error_log = []

        try:
            cf_df = pd.read_excel(file_path, sheet_name='Cashflow')
            twr_df = pd.read_excel(file_path, sheet_name='TWR')
            bm_df = pd.read_excel(file_path, sheet_name='Benchmark')
            config_df = pd.read_excel(file_path, sheet_name='Configuration')

            try: denom_df = pd.read_excel(file_path, sheet_name='Denom')
            except: denom_df = pd.DataFrame()

            try: sec_bm = pd.read_excel(file_path, sheet_name='Sector Benchmark')
            except: sec_bm = pd.DataFrame()

        except Exception as e:
            print(f"Error loading {os.path.basename(file_path)}: {e}")
            continue

        def parse_dates_robust(series):
            if pd.api.types.is_numeric_dtype(series): return pd.to_datetime(series, origin='1899-12-30', unit='D')
            try: return pd.to_datetime(series, errors='raise')
            except Exception:
                num_s = pd.to_numeric(series, errors='coerce')
                if not num_s.isna().all(): return pd.to_datetime(num_s, origin='1899-12-30', unit='D')
                return pd.to_datetime(series, errors='coerce')

        try:
            cf_df['Effective Date'] = parse_dates_robust(cf_df['Effective Date'])
            twr_df['Date'] = parse_dates_robust(twr_df['Date'])
            bm_df['Period'] = parse_dates_robust(bm_df['Period'])
            if not denom_df.empty:
                denom_df['Date'] = parse_dates_robust(denom_df['Date'])
        except Exception as e:
            error_log.append({'Module': 'Date Parsing', 'Entity/Group': 'Global', 'Error Details': f"Failed parsing dates: {str(e)}"})

        REPORTING_DATE = twr_df['Date'].max()

        inv_cols = [c for c in config_df.columns if str(c).strip().lower() == 'investor name']
        if inv_cols and not config_df[inv_cols[0]].dropna().empty: investor_name = str(config_df[inv_cols[0]].dropna().iloc[0]).strip()
        else: investor_name = "Unknown Investor"
        safe_investor_name = "".join([c for c in investor_name if c.isalnum() or c in ' -_']).strip()

        num_cf_cols = ['Contributions', 'Distributions', 'Ending NAV', 'Advisory Fee', 'Realized Incentive Fee', 'Unrealized Incentive Fee', 'Gross Cash Flow', 'Net Cash Flow']
        for c in num_cf_cols:
            if c in cf_df.columns: cf_df[c] = pd.to_numeric(cf_df[c], errors='coerce').fillna(0)

        num_twr_cols = ['Gross Investment Income Minus Fees', 'Total Gross Appreciation', 'Net Investment Income', 'Net Appreciation', 'Denominator', 'Gross Total Return', 'Net Total Return']
        for c in num_twr_cols:
            if c in twr_df.columns: twr_df[c] = pd.to_numeric(twr_df[c], errors='coerce').fillna(0)

        if not denom_df.empty:
            denom_df['Denominator'] = pd.to_numeric(denom_df['Denominator'], errors='coerce').fillna(0)

        # 1. Execute Module 2
        master_df, active_days_df, irr_val_df, twr_aggregate_df, composite_twr_df, indiv_twr, portfolio_sections = build_performance_summary(cf_df, twr_df, bm_df, config_df, investor_name, error_log, REPORTING_DATE, sec_bm)

        # 2. Execute Module 3
        trailing_irr_df, trailing_pivot, ent_pivot, final_breakdowns, final_entity_breakdowns, final_return_distributions, j_curve_export, abs_df, alpha_df, corr_matrices, rolling_corr_df, brinson_df, aum_pivot, decay_df = build_analytics(cf_df, twr_df, bm_df, config_df, indiv_twr, composite_twr_df, portfolio_sections, error_log, REPORTING_DATE, denom_df, sec_bm)

        # 3. Execute Module 5
        disclosure_df = get_disclosures()

        # 4. Execute Module 4
        excel_filename = os.path.join(FOLDER_PATH, f"Processed_Performance-Master {safe_investor_name}.xlsx")
        export_to_excel(excel_filename, master_df, active_days_df, irr_val_df, twr_aggregate_df, composite_twr_df, trailing_irr_df, trailing_pivot, ent_pivot, final_breakdowns, final_entity_breakdowns, final_return_distributions, j_curve_export, abs_df, alpha_df, corr_matrices, rolling_corr_df, error_log, disclosure_df, portfolio_sections, brinson_df, aum_pivot, decay_df)

    print("\nAll files have been processed!")
